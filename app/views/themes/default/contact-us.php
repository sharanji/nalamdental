                            <script>
                            $(document).ready(function () {
                                Captcha();

                                $('#name').on('input', function () {
                                    const name = $(this).val();
                                    validateName(name);
                                });

                                $('#email').on('input', function () {
                                    const email = $(this).val();
                                    validateEmail(email);
                                });

                                $('#phone').on('input', function () {
                                    const phone = $(this).val();
                                    validatePhoneNumber(phone);
                                });

                                $('#captcha').on('input', function () {
                                    const captcha = $(this).val();
                                    validateCaptcha(captcha);
                                });

                                $('#contactForm').on('submit', submitForm);
                            });

                            // Generate CAPTCHA
                            function Captcha() {
                                const captchaChars = 'ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789';
                                let captchaCode = '';
                                for (let i = 0; i < 6; i++) {
                                    captchaCode += captchaChars.charAt(Math.floor(Math.random() * captchaChars.length));
                                }

                                $('#mainCaptcha').text(captchaCode).attr("data-code", captchaCode); 
                            }

                            // Validation Functions
                            function validateName(name) {
                                if (name === '') {
                                    $('#nameError').text('Please enter your name.');
                                    $('#name').removeClass('is-valid').addClass('is-invalid');
                                } else if (!isValidName(name)) {
                                    $('#nameError').text('Invalid name format.');
                                    $('#name').removeClass('is-valid').addClass('is-invalid');
                                } else {
                                    $('#nameError').text('');
                                    $('#name').removeClass('is-invalid').addClass('is-valid');
                                }
                            }

                            function validateEmail(email) {
                                if (email === '') {
                                    $('#emailError').text('Please enter your email.');
                                    $('#email').removeClass('is-valid').addClass('is-invalid');
                                } else if (!isValidEmail(email)) {
                                    $('#emailError').text('Please enter a valid email address.');
                                    $('#email').removeClass('is-valid').addClass('is-invalid');
                                } else {
                                    $('#emailError').text('');
                                    $('#email').removeClass('is-invalid').addClass('is-valid');
                                }
                            }

                            function validatePhoneNumber(phone) {
                                if (phone === '') {
                                    $('#phoneError').text('Please enter your phone number.');
                                    $('#phone').removeClass('is-valid').addClass('is-invalid');
                                } else if (phone.length < 10) {
                                    $('#phoneError').text('Please enter a valid 10-digit phone number.');
                                    $('#phone').removeClass('is-valid').addClass('is-invalid');
                                } else if (!isValidPhoneNumber(phone)) {
                                    $('#phoneError').text('Invalid phone number.');
                                    $('#phone').removeClass('is-valid').addClass('is-invalid');
                                } else {
                                    $('#phoneError').text('');
                                    $('#phone').removeClass('is-invalid').addClass('is-valid');
                                }
                            }

                            function validateCaptcha(captcha) {
                                if (captcha === '') {
                                    $('#captchaError').text('Please enter the captcha.');
                                    $('#captcha').removeClass('is-valid').addClass('is-invalid');
                                } else if (!ValidCaptcha(captcha)) {
                                    $('#captchaError').text('Invalid captcha, please try again.');
                                    $('#captcha').removeClass('is-valid').addClass('is-invalid');
                                } else {
                                    $('#captchaError').text('');
                                    $('#captcha').removeClass('is-invalid').addClass('is-valid');
                                }
                            }

                            function clearErrors() {
                                $('#nameError, #emailError, #phoneError, #captchaError').text('');
                            }

                            // Form Submission
                            let isSubmitting = false; // Flag to track submission status

                            function submitForm(event) {
                                event.preventDefault();
                                if (isSubmitting) return;
                                isSubmitting = true;
                                $('#submitButton').prop('disabled', true);

                                let isValid = true;

                                const name = $('#name').val().trim();
                                const email = $('#email').val().trim();
                                const phone = $('#phone').val().trim();
                                const captcha = $('#captcha').val().trim();

                                clearErrors();

                                validateName(name);
                                validateEmail(email);
                                validatePhoneNumber(phone);
                                validateCaptcha(captcha);

                                if ($('#nameError').text() || $('#emailError').text() || $('#phoneError').text() || $('#captchaError').text()) {
                                    isValid = false;
                                }

                                if (isValid) {
                                    $('#contactForm')[0].submit();
                                } else {
                                    $('#submitButton').prop('disabled', false);
                                    isSubmitting = false;
                                }

                                return false;
                            }


                            // Validation Helpers
                            function ValidCaptcha(userCaptchaInput) {
                                const generatedCaptcha = $('#mainCaptcha').text().trim();
                                return generatedCaptcha === userCaptchaInput.trim();
                            }

                            function isValidEmail(email) {
                                return email.includes('@') && email.split('@')[1].includes('.');
                            }

                            function isValidPhoneNumber(phone) {
                                return phone.length === 10 && /^[0-9]+$/.test(phone); 
                            }

                            function isValidName(name) {
                                const namePattern = /^(?!.*\s{2})(?!.*^\s)[a-zA-Z]+(?: [a-zA-Z]+)*$/;
                                return namePattern.test(name);
                            }
